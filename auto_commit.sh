#!/bin/bash

# Скрипт для автоматического коммита и пуша изменений в репозиторий GitHub

# Проверяем, есть ли изменения
if git diff --quiet && git diff --staged --quiet; then
    echo "Нет изменений для коммита."
    exit 0
fi

# Добавляем все изменения
git add .

# Коммитим с сообщением
git commit -m "Автоматический коммит изменений"

# Пушим в origin main (или текущую ветку)
git push origin main

echo "Изменения успешно закоммичены и отправлены в GitHub."